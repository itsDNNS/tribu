#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const FRONTEND_ROOT = path.resolve(__dirname, '..');
const LANGUAGES_PATH = path.join(FRONTEND_ROOT, 'lib', 'i18nLanguages.json');
const I18N_DIR = path.join(FRONTEND_ROOT, 'i18n');
const OUTPUT_PATH = path.join(FRONTEND_ROOT, 'lib', 'generated', 'i18nBundles.js');
const LANGUAGE_KEY_PATTERN = /^[a-z]{2}(?:-[A-Z]{2})?$/;

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function assert(condition, message) {
  if (!condition) {
    throw new Error(message);
  }
}

function collectLocaleFiles() {
  return fs
    .readdirSync(I18N_DIR)
    .filter((file) => file.endsWith('.json'))
    .filter((file) => LANGUAGE_KEY_PATTERN.test(path.basename(file, '.json')))
    .sort();
}

function validateLanguages(languages, localeFiles) {
  assert(Array.isArray(languages), `${LANGUAGES_PATH} must contain an array`);

  const seen = new Set();
  for (const [index, language] of languages.entries()) {
    assert(language && typeof language === 'object', `language at index ${index} must be an object`);
    assert(typeof language.key === 'string' && language.key, `language at index ${index} needs a key`);
    assert(LANGUAGE_KEY_PATTERN.test(language.key), `invalid language key: ${language.key}`);
    assert(!seen.has(language.key), `duplicate language key: ${language.key}`);
    seen.add(language.key);

    for (const field of ['name', 'nativeName', 'version', 'author']) {
      assert(typeof language[field] === 'string' && language[field].trim(), `${language.key} needs ${field}`);
    }
  }

  const languageFiles = languages.map((language) => `${language.key}.json`).sort();
  const missing = languageFiles.filter((file) => !localeFiles.includes(file));
  const unregistered = localeFiles.filter((file) => !languageFiles.includes(file));

  assert(missing.length === 0, `registered languages without bundles: ${missing.join(', ')}`);
  assert(unregistered.length === 0, `locale bundles missing from language registry: ${unregistered.join(', ')}`);
  assert(seen.has('en'), 'English must be present for fallback messages');
}

function toIdentifier(languageKey) {
  return `locale_${languageKey.replace(/[^A-Za-z0-9_$]/g, '_')}`;
}

function generateIndex() {
  const languages = readJson(LANGUAGES_PATH);
  const localeFiles = collectLocaleFiles();
  validateLanguages(languages, localeFiles);

  const imports = languages
    .map((language) => `import ${toIdentifier(language.key)} from '../../i18n/${language.key}.json';`)
    .join('\n');

  const bundles = languages
    .map((language) => `  ${JSON.stringify(language.key)}: ${toIdentifier(language.key)},`)
    .join('\n');

  const metadata = languages
    .map((language) => {
      const { key, ...meta } = language;
      return `  ${JSON.stringify(key)}: ${JSON.stringify(meta)},`;
    })
    .join('\n');

  const keys = languages.map((language) => JSON.stringify(language.key)).join(', ');

  return `${imports}\n\n// This file is generated by scripts/generate-i18n-index.js. Do not edit by hand.\nexport const supportedLanguageKeys = [${keys}];\n\nexport const localeBundles = {\n${bundles}\n};\n\nexport const languageMeta = {\n${metadata}\n};\n`;
}

function main() {
  const check = process.argv.includes('--check');
  const nextContent = generateIndex();
  const currentContent = fs.existsSync(OUTPUT_PATH) ? fs.readFileSync(OUTPUT_PATH, 'utf8') : '';

  if (check) {
    if (currentContent !== nextContent) {
      console.error(`${path.relative(FRONTEND_ROOT, OUTPUT_PATH)} is out of date. Run npm run i18n:generate.`);
      process.exit(1);
    }
    console.log(`${path.relative(FRONTEND_ROOT, OUTPUT_PATH)} is up to date.`);
    return;
  }

  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  fs.writeFileSync(OUTPUT_PATH, nextContent);
  console.log(`Generated ${path.relative(FRONTEND_ROOT, OUTPUT_PATH)} from ${languagesSummary()}.`);
}

function languagesSummary() {
  const languages = readJson(LANGUAGES_PATH);
  return `${languages.length} languages`;
}

main();
