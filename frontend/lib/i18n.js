export function buildMessages(lang) {
  const safeLang = coreLocales[lang] ? lang : 'de';
  const merged = { ...coreLocales[safeLang] };
  for (const mod of moduleLocales[safeLang] || []) {
    Object.assign(merged, mod);
  }
  return merged;
}

